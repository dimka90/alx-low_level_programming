/**
*op_add - return the sum of and a and b
*@a:first operand
*@b:second operand
*Return:sum
*/
int op_add(int a, int b)
{
int result;
result = a + b;

return (result);
}
/**
*op_sub - return the difference  between a and b
*@a:first operand
*@b:second operand
*Return:subtract
*/
int op_sub(int a, int b)
{


int result;

result = a - b;

return (result);

}

/**
*op_mul - return the multiply of and a and b
*@a:first operand
*@b:second operand
*Return:difference
*/
int op_mul(int a, int b)
{

int result;
result = a * b;

return (result);

}

/**
*op_div - return the divisor of a and b
*@a:first operand
*@b:second operand
*Return:divisor remainder
*/
int op_div(int a, int b)
{


int result;
result = a / b;

return (result);
}

/**
*op_mod - return the remainder of  a and b
*@a:first operand
*@b:second operand
*Return:modulo
*/
int op_mod(int a, int b)
{

int result;
result = a % b;

return (result);
}
